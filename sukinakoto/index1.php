        <h2>Breaking Downとは</h2>
        <p>Breaking Downは、youtubeのコンテンツです。</p>
        <p>日本を代表する格闘家兼youtuberの朝倉未来が立ち上げた「1分間最強を決める。」というテーマの格闘技で素人でもプロに勝てる事を売りにしています。</p>
        <p>基本的には格闘技未経験者がリングの中で1対1で1分間のキックボクシングを行う大会です。</p>
        <p>2021年2月に第一回大会が行われ、昨年の11月に第10回のメモリアル大会が開催されました。</p>
